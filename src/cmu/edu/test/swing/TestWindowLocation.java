package cmu.edu.test.swing;

import static org.junit.Assert.assertTrue;

import org.junit.Test;

import cmu.edu.window.SyWindow;

/**
 * This class contains the test cases for the SyWindow.setLocation method.
 * 
 * @author Ruben Martins
 */
public class TestWindowLocation {
	
	@Test
	public void test1() {
		javax.swing.JFrame frame = new javax.swing.JFrame("title");
		frame = SyWindow.setLocation(frame, 200, 300);
		assertTrue(frame.getLocation().getX() == 200 &&
				   frame.getLocation().getY() == 300 && 
				   frame.getTitle().equals("title"));
	}
	
}
